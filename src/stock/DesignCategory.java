    @Action
    public void buttonSimpan() throws SQLException {
        if(getValidTextBox())
        {
            ClassCategory ctg = new ClassCategory();
            ctg.setCategory(jTextField2.getText());
            ctg.insertAllData();
            JOptionPane.showMessageDialog(null,"Data berhasil Disimpan !");
            loadView();
        }
    }

    @Action
    public void buttonNew() {
        jButtonUpdate.setEnabled(false);
        jButtonSimpan.setEnabled(true);
        jTextField1.setText("");
        jTextField2.setText("");
    }

    @Action
    public void buttonUpdate() throws SQLException {
        if(getValidTextBox())
        {
            ClassCategory ctg = new ClassCategory();
            ctg.setId(Integer.parseInt(jTextField1.getText()));
            ctg.setCategory(jTextField2.getText());
            ctg.updateData();
            JOptionPane.showMessageDialog(null,"Data berhasil Diupdate !");
            loadView();
        }
    }
    
    public boolean getValidTextBox()
    {
        if(jTextField2.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Mohon Isi Nama Kategori");
            return false;
        }
        return true;
    }
    
    private void tableListener() {       
        jTableCategory.addMouseListener(new MouseAdapter(){
            public void mouseClicked(MouseEvent e)
            {
                int row = jTableCategory.rowAtPoint(e.getPoint());
                int col = jTableCategory.columnAtPoint(e.getPoint());
                jButtonUpdate.setEnabled(true);
                        jButtonSimpan.setEnabled(false);
                        jTextField1.setText(jTableCategory.getValueAt(
                                row,0).toString());
                        jTextField2.setText(jTableCategory.getValueAt(
                                row,1).toString());
            }
        });
    }
    
