    private void loadView() throws SQLException {
       Object[] columns;
       Object[][] rows;
       ClassCategory ctg = new ClassCategory();
       ResultSet rslt = ctg.getAllData();
       TableModel tableModel;
       int i = 0;
       
       columns = new Object[2];
       columns[0] = "ID";
       columns[1] = "Kategori";
       
       rslt.last();
       rows = new Object[rslt.getRow()][2];
       rslt.beforeFirst();
       
       while(rslt.next())
       {
           rows[i][0] = rslt.getString("ID");
           rows[i][1] = rslt.getString("KATEGORI");
           System.out.println(rows[i][0]);
           System.out.println(rows[i][1]);
           i++;
       }
       
       tableModel = new DefaultTableModel(rows,columns);
       jTableCategory.setModel(tableModel);
    }

